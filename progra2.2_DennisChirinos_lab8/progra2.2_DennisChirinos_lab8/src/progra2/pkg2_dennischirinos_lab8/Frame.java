    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        SD.CrearFactura(listCliente.get(JD5_CB_Clientes.getSelectedIndex()));
        TA_Factura.setText("");
        Cliente c = listCliente.get(JD5_CB_Clientes.getSelectedIndex());
        double Total = 0;
        TA_Factura.append("Nombre: " + c.getNombre() + "\n");
        TA_Factura.append("Edad: " + c.getEdad() + "\n");
        TA_Factura.append("\n");
        for (int i = 0; i < c.listProductos.size(); i++) {
            double x = Math.round(c.listProductos.get(i).Precio * (1 - c.listProductos.get(i).getDescuento()));
            TA_Factura.append(c.listProductos.get(i).getNombre()
                    + ": " + " " + (x) + "\n");
            Total += x;
        }
        TA_Factura.append("Total: " + Total);
        Total_De_Ventas += Total;
        jDialog2.pack();
        jDialog2.setVisible(true);
    }//GEN-LAST:event_jButton4ActionPerformed

