    private void jMenuItem5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem5ActionPerformed
        jDialog4.pack();
        jDialog4.setVisible(true);
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nombre");
        model.addColumn("Categoria");
        model.addColumn("Precio");
        model.addColumn("Descuento");
        if (listProducto.size() >= 0) {
            Object[] o = new Object[model.getColumnCount()];
            for (int i = 0; i < listProducto.size(); i++) {
                o[0] = listProducto.get(i).getNombre();
                o[1] = listProducto.get(i).getCategoria();
                o[2] = listProducto.get(i).getPrecio();
                o[3] = listProducto.get(i).getDescuento();
                model.addRow(o);
            }
        }
        Tabla.setModel(model);
    }//GEN-LAST:event_jMenuItem5ActionPerformed

