    public static Producto CrearProducto(int Serie) {
        String Nombre = (JOptionPane.showInputDialog("Nombre: "));
        String Categoria = (JOptionPane.showInputDialog("Categoria: "));
        double Precio = Double.parseDouble(JOptionPane.showInputDialog("Precio: "));
        double Descuento = Double.parseDouble(JOptionPane.showInputDialog("Descuento: "));
        Producto c = new Producto(Nombre, Categoria, Precio, Descuento);
        return c;
    }

    public void actualizar() {
        DefaultListModel modelTienda = new DefaultListModel();
        for (int i = 0; i < listProducto.size(); i++) {
            modelTienda.addElement(listProducto.get(i));
        }
        DefaultListModel modelCarrito = new DefaultListModel();
        for (int i = 0; i < listCliente.get(JD5_CB_Clientes.getSelectedIndex()).listProductos.size(); i++) {
            modelCarrito.addElement(listCliente.get(JD5_CB_Clientes.getSelectedIndex()).listProductos.get(i));
        }
        JD5_JL_Carrito.setModel(modelCarrito);
        JD5_JL_Tienda.setModel(modelTienda);
        Total_de_Ventas.setText("Total de Ventas: " + Total_De_Ventas);
    }

    public static Cliente CrearCliente(int Serie) {
        String Nombre = (JOptionPane.showInputDialog("Nombre: "));
        int Edad = Integer.parseInt(JOptionPane.showInputDialog("Edad: "));
        Cliente c = new Cliente(Nombre, Edad);
        return c;
    }

    public void isUsuario() {
        String op = JOptionPane.showInputDialog("Es usted Usuario o Cliente?" + "\n"
                + "si = Usuario" + "\n"
                + "no/otra cosa = Cliente" + "\n");
        if (op.equals("si")) {
            jMenu1.setVisible(true);
            jMenu2.setVisible(true);
        } else {
            jMenu1.setVisible(false);
            jMenu2.setVisible(false);
            Total_de_Ventas.setVisible(false);
        }
    }
